// è¯­è¨€è®¾ç½®ç®¡ç†
export class LanguageManager {
    constructor() {
        this.loadPreference();
    }

    loadPreference() {
        const saved = localStorage.getItem('blockly_language_mode');
        this.isPinyinMode = saved === 'pinyin';
    }

    savePreference() {
        localStorage.setItem('blockly_language_mode', this.isPinyinMode ? 'pinyin' : 'chinese');
    }

    setPinyinMode(enabled) {
        this.isPinyinMode = enabled;
        this.savePreference();
    }

    toggle() {
        this.isPinyinMode = !this.isPinyinMode;
        this.savePreference();
        return this.isPinyinMode;
    }

    translate(text) {
        if (!this.isPinyinMode) return text;
        return PINYIN_TRANSLATIONS[text] || text;
    }

    // æ‰¹é‡ç¿»è¯‘DOMå…ƒç´ 
    translatePage() {
        // ç¿»è¯‘æ‰€æœ‰å¸¦æœ‰ data-i18n å±æ€§çš„å…ƒç´ 
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translated = this.translate(key);
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = translated;
            } else {
                el.textContent = translated;
            }
        });

        // æ›´æ–°æŒ‰é’®æ–‡æœ¬ï¼ˆä¿ç•™emojiï¼‰
        document.querySelectorAll('[data-i18n-btn]').forEach(el => {
            const key = el.getAttribute('data-i18n-btn');
            const btnKey = el.getAttribute('data-btn-emoji') || '';
            const translated = this.translate(key);
            el.textContent = btnKey + ' ' + translated;
        });

        // ç¿»è¯‘placeholder
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.translate(key);
        });
    }
}

